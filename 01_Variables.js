// LET -  VARIABLE CANNOT BE RE-DECLARED BUT CAN BE UPDATED

let age=10;
console.log(`age ${age}`);

// VAR - VARIABLE CAN BE RE-DECLARED & UPDATED 

var x=10;
console.log(`x value ${x}`);
x=20;
console.log(`x value ${x}`);


// CONST - VARIABLE CANNOT BE RE-DECLARED & UPDATED\

const value=10;
console.log(`value is ${value}`);