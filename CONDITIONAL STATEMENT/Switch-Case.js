// Switch case -->to specify many alternative blocks of code to be executed

let marks=1
let Grade;
switch(marks){
    case 1:
        Grade="A"
        break;
    case 2:
        Grade="B"
        break;
    case 3:
        Grade="C"
        break;
    case 4:
        Grade="D"
        break;
    case 5:
        Grade="E"
        break;
    case 6:
        Grade="F"
        break;
    default:
        Grade="Invalid"
}
console.log(Grade);